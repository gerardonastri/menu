// Funzione helper per tradurre gli elementi del menu
export function translateMenuItem(item, language) {
  if (language === "it") return item;

  const translatedItem = { ...item };

  // Traduci il nome
  if (menuItemTranslations[item.name]) {
    translatedItem.name = menuItemTranslations[item.name];
  }

  // Traduci la descrizione
  if (item.description && menuDescriptionTranslations[item.description]) {
    translatedItem.description = menuDescriptionTranslations[item.description];
  }

  // Traduci i valori dei campi
  Object.keys(item).forEach((key) => {
    if (typeof item[key] === "string" && menuValueTranslations[item[key]]) {
      translatedItem[key] = menuValueTranslations[item[key]];
    }
  });

  return translatedItem;
}

// Funzione per tradurre le etichette dei campi
export function translateFieldLabel(fieldName, language) {
  return menuFieldTranslations[language][fieldName] || fieldName;
}
